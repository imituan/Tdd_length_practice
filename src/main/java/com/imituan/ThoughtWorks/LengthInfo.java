package com.imituan.ThoughtWorks;

public class LengthInfo
{
    private String name;
    private int value;
    private int relativeValue;
    private int relativeRate;

    public LengthInfo()
    {
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public int getValue()
    {
        return value;
    }

    public void setValue(int value)
    {
        this.value = value;
    }

    public int getRelativeValue()
    {
        return relativeValue;
    }

    public void setRelativeValue(int relativeValue)
    {
        this.relativeValue = relativeValue;
    }

    public int getRelativeRate()
    {
        return relativeRate;
    }

    public void setRelativeRate(int relativeRate)
    {
        this.relativeRate = relativeRate;
    }
}